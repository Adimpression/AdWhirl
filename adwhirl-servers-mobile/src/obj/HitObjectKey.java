package obj;

public class HitObjectKey {
	public String nid;
    public String aid;
	
    public HitObjectKey(String nid, String aid) {
    	this.nid = nid;
    	this.aid = aid;
    }

    // hashCode() and equals() generated by Eclipse
    
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((nid == null) ? 0 : nid.hashCode());
		result = prime * result + ((aid == null) ? 0 : aid.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		
		HitObjectKey other = (HitObjectKey) obj;
		
		if (nid == null) {
			if (other.nid != null) {
				return false;
			}
		} 
		else if (!nid.equals(other.nid)) {
			return false;
		}
		
		if (aid == null) {
			if (other.aid != null) {
				return false;
			}
		} 
		else if (!aid.equals(other.aid)) {
			return false;
		}
		
		return true;
	}

}
